package tic_tac_toe;

public class ModelSettings {

	double alpha;
	double chanceRandomMove;
	
	public double getAlpha() {
		return alpha;
	}
	public void setAlpha(double alpha) {
		this.alpha = alpha;
	}
	public double getChanceRandomMove() {
		return chanceRandomMove;
	}
	public void setChanceRandomMove(double chanceRandomMove) {
		this.chanceRandomMove = chanceRandomMove;
	}
	
}
